/*****************************************************************************
 结构名    : CPROC_1X_NVIM_DM_THRESHOLD_STRU
 协议表格  :
 ASN.1描述 :
结构说明  : 该NV用于存储cproc_1x对分集天线控制的相关参数。
*****************************************************************************/
typedef struct
{
    /*
     * 动态分集天线控制功能开关。
     * 0：关闭（动态分集关闭时，分集开关不需要动态控制，一直处于打开状态）
     * 1：打开
     */
    VOS_UINT8                           ucDiversitySwitch;
    /*
     * 空闲态PCH CRC校验值计数门限初始值。
     * 当CRC校验连续正确个数达到该值，分集天线将关闭。
     * 单位：个
     */
    VOS_UINT8                           ucIdleCrcInitCounter;
    /*
     * SIM卡温度高于该值时，分集天线将关闭，低于该值则允许打开。
     * 单位：0.1℃
     */
    VOS_UINT16                          usSimCardTemperature;
    /*
     * 空闲态RSCP测量值低于该门限则打开分集天线。
     * 单位：1/8 dBm
     */
    VOS_INT16                           shwIdleMeasRscpOn;
    /*
     * 空闲态RSCP测量值高于该门限则允许关闭分集天线。
     * 单位：1/8 dBm
     */
    VOS_INT16                           shwIdleMeasRscpOff;
    /*
     * 空闲态EcN0测量值低于该门限则打开分集天线。
     * 单位：1/8dB
     */
    VOS_INT16                           shwIdleMeasEcN0On;
    /*
     * 空闲态EcN0测量值高于该门限则允许关闭分集天线。
     * 单位：1/8dB
     */
    VOS_INT16                           shwIdleMeasEcN0Off;
    /*
     * TCH态CS业务FER误帧个数门限，在统计窗
     * 单位：个
     */
    VOS_UINT8                           ucTchCSFerOnCounter;
    /*
     * TCH态CS业务FER误帧个数门限，在统计窗
     * 单位：个
     */
    VOS_UINT8                           ucTchCSFerOffCounter;
    /*
     * TCH态CS业务开分集的误帧个数统计窗，在统计窗内达到门限
     * 单位：个
     */
    VOS_UINT8                           ucTchCSFerOnWinSize;
    /*
     * TCH态CS业务关分集的误帧个数统计窗，在统计窗内未达到门限
     * 单位：个
     */
    VOS_UINT8                           ucTchCSFerOffWinSize;
}CPROC_1X_NVIM_DM_THRESHOLD_STRU;


